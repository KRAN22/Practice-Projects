//  Write a function which accepts a number and prints the multiplication table upto 10. (Don't use any loops yet)

function table(num1) {
  console.log(num1 + "*" + 1 + "=" + num1 * 1);

  console.log(num1 + "*" + 2 + "=" + num1 * 2);

  console.log(num1 + "*" + 3 + "=" + num1 * 3);

  console.log(num1 + "*" + 4 + "=" + num1 * 4);

  console.log(num1 + "*" + 5 + "=" + num1 * 5);

  console.log(num1 + "*" + 6 + "=" + num1 * 6);

  console.log(num1 + "*" + 7 + "=" + num1 * 7);

  console.log(num1 + "*" + 8 + "=" + num1 * 8);

  console.log(num1 + "*" + 9 + "=" + num1 * 9);

  console.log(num1 + "*" + 10 + "=" + num1 * 10);
}

table(9);
