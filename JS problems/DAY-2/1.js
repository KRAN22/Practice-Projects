// Write 4 functions add, mul, sub, div which will accepts two params a, b and returns the arithmetical results.

// addition

function add(a, b) {
  return a + b;
}

let result = add(4, 5);

console.log(result);

// multiplication

function mul(a, b) {
  return a * b;
}

var result1 = mul(4, 5);

console.log(result1);

// subtraction

function sub(a, b) {
  return a - b;
}
var result2 = sub(5, 4);
console.log(result2);

//division

function div(a, b) {
  return a / b;
}
var result3 = div(4, 0);
console.log(result3);
